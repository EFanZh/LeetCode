pub mod naive;
pub mod single_pass;

pub trait Solution {
    fn is_valid_sudoku(board: Vec<Vec<char>>) -> bool;
}

#[cfg(test)]
mod tests {
    use super::Solution;

    pub fn run<S: Solution>() {
        let test_cases = [
            (
                [
                    "53..7....",
                    "6..195...",
                    ".98....6.",
                    "8...6...3",
                    "4..8.3..1",
                    "7...2...6",
                    ".6....28.",
                    "...419..5",
                    "....8..79",
                ],
                true,
            ),
            (
                [
                    "83..7....",
                    "6..195...",
                    ".98....6.",
                    "8...6...3",
                    "4..8.3..1",
                    "7...2...6",
                    ".6....28.",
                    "...419..5",
                    "....8..79",
                ],
                false,
            ),
            (
                [
                    "7...4....",
                    "...865...",
                    ".1.2.....",
                    ".....9...",
                    "....5.5..",
                    ".........",
                    "......2..",
                    ".........",
                    ".........",
                ],
                false,
            ),
            (
                [
                    "..4...63.",
                    ".........",
                    "5......9.",
                    "...56....",
                    "4.3.....1",
                    "...7.....",
                    "...5.....",
                    ".........",
                    ".........",
                ],
                false,
            ),
            (
                [
                    "....5..1.",
                    ".4.3.....",
                    ".....3..1",
                    "8......2.",
                    "..2.7....",
                    ".15......",
                    ".....2...",
                    ".2.9.....",
                    "..4......",
                ],
                false,
            ),
        ];

        for (board, expected) in test_cases {
            let result = S::is_valid_sudoku(board.iter().map(|row| row.chars().collect()).collect());

            assert_eq!(result, expected);
        }
    }
}
